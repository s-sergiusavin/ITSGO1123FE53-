/**
 ########### Console - Debugger - shortcuts###########
 #####################################################
- Continue: Command + / (forward slash)
- Step over: Command + ' (apostrophe)
- Step into: Command + ; (semi-colon)
- Step out: Shift-Command + ; (semi-colon)
- Next call frame: Control + . (period)
- Previous call frame: Control + , (comma)


 */